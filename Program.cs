// 네임스페이스 : 클래스이름의 중복제거한다.
namespace Examples
{
    // internal class : 내부클래스를 의미
    internal class Program
    {
        // static void Main () : 프로그램 시작점(Entry Point)
        static void Main(string[] args)
        {
            // Console.WriteLine : 콘솔창에 문자열 함수 출력
            Console.WriteLine("Hello, World!");
        }
    }
}
